console.log(Math) // Is a Object
console.log("Absolute :- " , Math.abs(-12)); // - to +
console.log("Ceil :- " , Math.ceil(4.2)); // Always Choose Upper Value
console.log("Floor :- " , Math.floor(4.2)); // Always Choose Lower Value
console.log("Square Root :- " , Math.sqrt(9));
console.log("Max Value :- " , Math.max(1,2,3));
console.log("Min Value :- " , Math.min(1,2,3));
console.log("Power :- " , Math.pow(2,3));
console.log("Default Range :- " , Math.random()); // Default Range 0 to 1
console.log("Round :- " , Math.round(4.7));











